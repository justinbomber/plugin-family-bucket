/**
 * 將 Markdown Viewer legacy 背景腳本打包成 ESM，供 type:module 的 Service Worker 靜態 import。
 * 執行：node plugin-family-bucket/scripts/bundle-markdown-sw.mjs
 */
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const MV = path.join(ROOT, "plugins", "markdown-viewer");

const FILES = [
  "vendor/markdown-it.min.js",
  "vendor/marked.min.js",
  "vendor/remark.min.js",
  "background/compilers/markdown-it.js",
  "background/compilers/marked.js",
  "background/compilers/remark.js",
  "background/storage.js",
  "background/webrequest.js",
  "background/detect.js",
  "background/inject.js",
  "background/messages.js",
  "background/mathjax.js",
  "background/xhr.js",
  "background/icon.js",
  "background/suite-init.js",
];

const outPath = path.join(MV, "background", "markdown-sw.bundle.mjs");

let body = "/* Auto-generated by scripts/bundle-markdown-sw.mjs — do not edit */\n";

for (const rel of FILES) {
  const abs = path.join(MV, rel);
  const chunk = await fs.readFile(abs, "utf8");
  body += `\n/* ----- ${rel} ----- */\n`;
  body += chunk;
  if (!chunk.trimEnd().endsWith(";")) body += "\n;";
  body += "\n";
}

body += `
export function bootstrapMarkdownSuiteBackground() {
  if (typeof md === "undefined" || typeof md.bootstrapMarkdownSuiteBackground !== "function") {
    throw new Error("[markdown-viewer] incomplete bundle — run bundle-markdown-sw.mjs");
  }
  md.bootstrapMarkdownSuiteBackground();
}
`;

await fs.writeFile(outPath, body, "utf8");
console.log("[bundle-markdown-sw] wrote", outPath);
